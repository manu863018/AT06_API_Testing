def calculateAgeInDays(age):
    """Function to calculate an age in days"""
    return age * 365


def calculateAgeInHours(age):
    """Function to calculate an age in hours"""
    return age * 365 * 24


def calculateAgeInMinutes(age):
    """Function to calculate an age in minutes"""
    return age * 365 * 24 * 60

